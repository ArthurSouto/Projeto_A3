    private void BT_ExcluirUserActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_BT_ExcluirUserActionPerformed
        String selectedUsuarioEmail = (String) CB_Users.getSelectedItem();
        Usuario selectedUsuario = usuarioMap.get(selectedUsuarioEmail);
        dao.deleteUsuario(selectedUsuario.getEmail());

        CB_Users.removeItem(selectedUsuarioEmail);
    }//GEN-LAST:event_BT_ExcluirUserActionPerformed

