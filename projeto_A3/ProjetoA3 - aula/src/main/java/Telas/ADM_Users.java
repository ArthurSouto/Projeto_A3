    private void CB_showList(){
        List<Usuario> listaUsuarios = dao.listaUsuarios();
        for(Usuario usuario : listaUsuarios){
            CB_Users.addItem(usuario.getEmail());
            usuarioMap.put(usuario.getEmail(),usuario);
        }
    }
    
